                        <h1>FAQs</h1>
                        
                        <p>As we receive questions and comments we will be posting them here. Please email us with anything you'd like clarified: enquiries@seedingsquare.co.uk. Thanks!</p>

		<ul class="faq">
            <li class="q">Do I poke all the holes?</li><li class="a">Not unless you want plant warfare in your garden! For optimum results poke and seed the holes of only ONE color for each press of the square. This gives each plant the prefect amount of space.</li>

            <li class="q">Is this a kid’s toy or a garden tool? </li><li class="a">It does look like a toy, but it’s a garden tool for anyone planting a vegetable garden (including kids). We chose bright colors to encourage children to help their parents in the garden and learn to be little green thumbs, plus the bright colors make it easier to spot so as not to get run over by the lawn mower!</li>

            <li class="q">Do I need to laminate my Planting Guide?</li> <li class="a">You can, but it’s not necessary.The see-through plastic bag that the Seeding Square comes in has a pocket on the inside. Just leave the guide in the pocket, make sure the zipper is closed, and bring the bag out to the garden without fear of your guide being damaged. The bag is also great for keeping all your pieces together for storage.</li>
			
            <li class="q">What if I damage my Planting Guide?</li><li class="a">No worries, on this page you’ll find PDFs of all the guides and instructions available to print off, including a bonus guide called the Companion Planting Guide, which tells you which plants grow best together.</li>

            <li class="q">Is it only for small gardens?</li> <li class="a">Definitely not! It works great for all gardens. For those with lots of space the Seeding Square can be used to plant rows too...you’re not confined to have a patch-work-quilt look to your garden. All you do is stamp out the same type of vegetable over and over, and viola...you have a row! With big gardens you’ll really notice how much time you save, as in optimizing your space so thoroughly, those pesky weeds won’t have nearly as much room to grow in!</li>

            <li class="q">Will I get as many vegetables out of my garden if I use a Seeding Square?</li> <li class="a">Yes! In fact you’ll get 2-5 times the harvest as compared to traditional row gardening. If you get more than you bargained for ... Share the wealth! I bet you have some friends, family and neighbors that would love to help you out with your fresh home-grown garden bounty.</li>

            <li class="q">How many Seeding Squares do I need?</li> <li class="a">One should do the trick. It’s a garden tool to use on planting day...like a shovel or a hoe. Once you’ve used it to plant your garden, simply rinse it off, and put it away for your next planting day. But it does make a great gift...so maybe get two instead ;)</li>
			
            <li class="q">What if I want to plant something that isn't on the list?</li><li class="a"> Not a problem. Simply Google the spacing needed for whatever you want to plant, and use the holes that match the suggested spacing the closest. Remember...this is gardening...not rocket science. For example, I was asked about Collard Greens the other day...Turns out, they're one plant per press. </li>

            <li class="q">What if everything grows too close together?</li> <li class="a">Depending on your climate type, soil nutrients, amount of water, or any number of other factors you will find your plant sizes can vary. Simply make note of any changes you would make for your next planting day and slip them into the pocket of your storage bag along with your Chart &amp; Instructions. You'll be ready to go for your next round!</li>
			
            <li class="q">My carrots twisted around each other. Help!!!</li> <li class="a">This just means that too many seeds ended up on each hole. You can fix this by pruning out a few as they grow or, we actually have a neat little trick for planting carrots! First poke all the red holes as usual, then lift the Seeding Square and play connect-the-dots making four troughs about and inch deep with your dibber. Next sprinkle your carrot seeds into the troughs about 1/2-1 inch apart. Cover and water as usual. Mmmmmmm carrots! </li>

            <li class="q">How big is the Seeding Square?</li> <li class="a"> The face plate is a foot by a foot and the side walls are an inch in depth. It brings the concept of Square-Foot-Gardening to a whole new level! </li>

            <li class="q">If I'm planting seedlings can I still use the Seeding Square? </li><li class="a">You bet! Use the Seeding Square to poke the appropriate holes in the soil, now instead of dropping in seeds, you will use the poked holes as your indicator of where to plant each seedling.</li>
